#include<iostream>

using namespace std;

int main() {

	int a = 10;
	int b = 5;

	cout << "a + b = " << a + b << endl;
	cout << "a - b = " << a - b << endl;

	int c = a * b;
	cout << "a * b = " << c << endl;

	cout << "a / b = " << a/b << endl;

	int x = 9;
	int y = 4;

	cout << "x / y = " << x / (y * 1.0) << endl;

	return 0;
}